package edu.maimonides.multimedia.shapes4learn.model;

public class Color {

	/**
	 * Red.
	 */
	private int r;

	/**
	 * Green.
	 */
	private int g;

	/**
	 * Blue.
	 */
	private int b;

	public Color() {
	}

	public void setRed(int red) {
		this.r = red;
	}

	public int getRed() {
		return this.r;
	}

	public void setGreen(int green) {
		this.g = green;
	}

	public int getGreen() {
		return this.g;
	}

	public void setBlue(int blue) {
		this.b = blue;
	}

	public int getBlue() {
		return this.b;
	}
}